function initRadio(audioElementId, trackTitleElementId) {
  const audioEl = document.getElementById(audioElementId);
  const titleEl = document.getElementById(trackTitleElementId);
  if (!audioEl) return;

  // Asegurar que no se pueda descargar
  audioEl.setAttribute('controlsList', 'nodownload');

  let currentPlaylist = [];
  let currentIndex = 0;
  let currentSelection = '';
  let hasSelected = false;

  function loadTrack(index) {
    if(currentPlaylist.length === 0) return;
    const url = currentPlaylist[index];
    audioEl.src = url;
    audioEl.load();
    if (titleEl) {
      const parts = url.split('/');
      let name = parts[parts.length - 1].split('.')[0];
      name = decodeURIComponent(name).replace(/_/g, ' ');
      titleEl.textContent = "Reproduciendo: " + name;
    }
  }

  function playAllRandom() {
    currentSelection = 'Escuchemos un poco de Todo';
    currentPlaylist = [];
    for (const folder in tracksData) {
      currentPlaylist = currentPlaylist.concat(tracksData[folder]);
    }
    currentPlaylist.sort(() => Math.random() - 0.5);
    currentIndex = 0;
    loadTrack(currentIndex);
    audioEl.play().catch(e => console.log('Autoplay prevent:', e));
  }

  function playFolder(folderName) {
    currentSelection = folderName;
    currentPlaylist = [...tracksData[folderName]];
    currentIndex = 0;
    loadTrack(currentIndex);
    audioEl.play().catch(e => console.log('Autoplay prevent:', e));
  }

  function showRadioOptions() {
    const dialog = document.createElement('dialog');
    dialog.style.cssText = 'background: #111; color: #eee; border: 1px solid #444; border-radius: 12px; padding: 2rem; max-width: 500px; text-align: center; font-family: sans-serif;';
    
    let html = '<h3 style="color: #a970ff; margin-top:0;">Selecciona una Estación</h3>';
    html += '<p style="margin-bottom: 1.5rem;">Elige la música que deseas escuchar:</p>';
    html += '<div style="display: flex; flex-direction: column; gap: 0.5rem;">';
    
    const folders = Object.keys(tracksData);
    folders.forEach(folder => {
      const label = folder.replace(/_/g, ' ');
      html += `<button class="radio-opt-btn" data-folder="${folder}" style="background: transparent; color: #fff; border: 1px solid #555; padding: 0.75rem; border-radius: 8px; cursor: pointer; transition: all 0.2s;">${label}</button>`;
    });
    
    html += `<button class="radio-opt-btn" data-folder="ALL" style="background: #a970ff; color: #fff; border: none; padding: 0.75rem; border-radius: 8px; cursor: pointer; margin-top: 0.5rem; font-weight: bold;">Escuchemos un poco de Todo</button>`;
    html += '</div>';
    
    dialog.innerHTML = html;
    document.body.appendChild(dialog);
    dialog.showModal();

    dialog.querySelectorAll('.radio-opt-btn').forEach(btn => {
      btn.addEventListener('click', (e) => {
        const folder = e.target.getAttribute('data-folder');
        hasSelected = true;
        if (folder === 'ALL') {
          playAllRandom();
        } else {
          playFolder(folder);
        }
        dialog.close();
        dialog.remove();
      });
    });
    
    dialog.addEventListener('cancel', () => {
      dialog.remove();
      // Si cancela y no ha elegido, reproducir todo por defecto
      if (!hasSelected) { hasSelected = true; playAllRandom(); }
    });
  }

  audioEl.addEventListener('play', (e) => {
    if (!hasSelected) {
      e.preventDefault();
      audioEl.pause();
      showRadioOptions();
    }
  });

  audioEl.addEventListener('ended', () => {
    currentIndex++;
    if (currentIndex >= currentPlaylist.length) {
      if (currentSelection === 'Escuchemos un poco de Todo') {
         // Ya terminó de escuchar todo, se vuelve a barajar
         playAllRandom();
      } else {
         // Preguntar si quiere repetir
         const repeat = confirm(`Has terminado de escuchar la estación "${currentSelection.replace(/_/g, ' ')}". ¿Deseas volver a escucharla? Si cancelas, se reproducirán todas de manera aleatoria.`);
         if (repeat) {
           playFolder(currentSelection);
         } else {
           playAllRandom();
         }
      }
    } else {
      loadTrack(currentIndex);
      audioEl.play().catch(e => console.log('Autoplay prevent:', e));
    }
  });

  // Next button
  const nextBtn = document.getElementById(audioElementId + '-next');
  if (nextBtn) {
    nextBtn.addEventListener('click', () => {
      if (!hasSelected) {
        showRadioOptions();
        return;
      }
      currentIndex = (currentIndex + 1) % currentPlaylist.length;
      loadTrack(currentIndex);
      audioEl.play().catch(e => console.log('Autoplay prevent:', e));
    });
  }
  
  // Set initial source so it's not empty, but don't mark as selected
  const allTracks = [];
  for (const folder in tracksData) allTracks.push(...tracksData[folder]);
  if (allTracks.length > 0) {
     audioEl.src = allTracks[Math.floor(Math.random() * allTracks.length)];
     audioEl.load();
  }
}

document.addEventListener('DOMContentLoaded', () => {
  initRadio('radio-player', 'radio-title');
});
